export const translations: Record<Locale, TranslationStrings> = {
  en, ko, ja, zh, es, fr, ar, de, pt, ru, hi, tr, uk, vi, th,
};

export function detectLocale(): Locale {
  if (typeof window === "undefined") return "en";
  const stored = localStorage.getItem("conflictbrief-locale");
  if (stored && stored in translations) return stored as Locale;
  const browserLang = navigator.language.split("-")[0].toLowerCase();
  if (browserLang in translations) return browserLang as Locale;
  return "en";
}

export function t(strings: TranslationStrings, key: keyof TranslationStrings, params?: Record<string, string | number>): string {
  let str = strings[key];
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      str = str.replace(`{${k}}`, String(v));
    }
  }
  return str;
}
